</div><em><br />Abstract:<br /></em><br />While the types of Computer Science experiments we can imagine are limited only by our creativity, in practice we can carry out only those that are supported by an instrument that allows us to deploy, capture, and&nbsp;record relevant scientific phenomena. Over the last several years Computer Science testbeds such as GENI and NSFCloud have significantly contributed towards developing experimental testbeds that make the&nbsp;deployment of a diversity of experiments feasible. What features are needed to transform those testbeds into scientific instruments that enable us to easily and accurately capture, and record as well as deploy? What&nbsp;type of support and features do we need for the support of repeatability and reproducibility? What can Computer Science testbeds do to improve experimental methodology?<br />
In this presentation I will introduce Chameleon, an NSFCloud open, deeply reconfigurable testbed supporting Computer Science experimentation at large scale. With 15,000+ cores, 5 PB of storage, and sites connected&nbsp;by 100G networks &ndash; and now extended for phase 2 which will bring even more exciting capabilities to its thousands of users &ndash; Chameleon is uniquely positioned to provide tools and encourage practices leading to&nbsp;better Computer Science experiments. I will describe the system, discuss new hardware and features that we will introduce in the coming years, and give examples of experiments our users are running. I will then&nbsp;describe support how we will leverage the testbed features to provide support for experimental methodology and repeatability in the current phase of system development.&nbsp;<br /><br /><em>About the speaker::<br /><br /></em>Kate Keahey is one of the pioneers of infrastructure cloud computing. She created the Nimbus project, recognized as the first open source Infrastructure-as-a-Service implementation, and engages in many application&nbsp;projects enabling the use of the cloud computing platform in science. She is currently leading the Chameleon project, a large scale, deeply reconfigurable experimental testbed for cloud computing research and a co-founder of SoftwareX, a new journal format established to publish software contributions. Kate is a Scientist at Argonne National Laboratory and a Senior Fellow at the Computation Institute at the University of&nbsp;Chicago.&nbsp;<br /></div></div></div><div class="re-col-desk-4 re-col-tabl-4 re-col-tab-6 re-col-mob-12"><div id='stacks_out_139_page14' class='stacks_out'><div id='stacks_in_139_page14' class='stacks_in '><div id='stacks_out_p139_n53_page14' class='stacks_out'><div id='stacks_in_p139_n53_page14' class='stacks_in '><div id='stacks_out_pp139_n53_n7_page14' class='stacks_out'><div id='stacks_in_pp139_n53_n7_page14' class='stacks_in text_stack'><h3>Hosting Orga