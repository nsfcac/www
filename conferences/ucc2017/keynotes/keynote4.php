</div><br /><em>Abstract:<br /></em><br />We look again at Big Data Programming environments such as Hadoop, Spark, Flink, Heron, Pregel; HPC concepts such as MPI and Asynchronous Many-Task runtimes and Cloud/Grid/Edge ideas such as event-driven computing, serverless computing, workflow, and Services. These cross many research communities including distributed systems, databases, cyberphysical systems and parallel computing which sometimes have inconsistent worldviews. There are many common capabilities across these systems which are often implemented differently in each packaged environment. For example, communication can be bulk synchronous processing or data flow; scheduling can be dynamic or static; state and fault-tolerance can have different models; execution and data can be streaming or batch, distributed or local. We suggest that one can usefully build a toolkit (called Twister2 by us) that supports these different choices and allows fruitful customization for each application area. We illustrate the design of Twister2 by several point studies. We stress the many open questions in very traditional areas including scheduling, messaging and checkpointing.<br /><br /><em>About the speaker:<br /></em><br />Fox received a Ph.D. in Theoretical Physics from Cambridge University where he was Senior Wrangler. He is now a distinguished professor of Engineering, Computing, and Physics at Indiana University where he is director of the Digital Science Center, and both Department Chair and Interim Associate Dean for Intelligent Systems Engineering at the School of Informatics, Computing, and Engineering.  He previously held positions at Caltech, Syracuse University, and Florida State University after being a postdoc at the Institute for Advanced Study at Princeton, Lawrence Berkeley Laboratory, and Peterhouse College Cambridge. He has supervised the Ph.D. of 70 students and published around 1300 papers (over 450 with at least 10 citations) in physics and computing with an hindex of 76 and over 31000 citations. He is a Fellow of APS (Physics) and ACM (Computing) and works on the interdisciplinary interface between computing and applications.<br /></div></div></div><div class="re-col-desk-4 re-col-tabl-4 re-col-tab-6 re-col-mob-12"><div id='stacks_out_139_page17' class='stacks_out'><div id='stacks_in_139_page17' class='stacks_in '><div id='stacks_out_p139_n53_page17' class='stacks_out'><div id='stacks_in_p139_n53_page17' class='stacks_in '><div id='stacks_out_pp139_n53_n7_page17' class='stacks_out'><div id='stacks_in_pp139_n53_n7_page17' class='stacks_in text_stack'><h3>Hosting Organizations</h3></div></div><div id='stacks_out_pp139_n53_n9_page17' class='stacks_out'><div id='stacks_in_pp139_n53_n9_page17' class='stacks_in image_stack'>
