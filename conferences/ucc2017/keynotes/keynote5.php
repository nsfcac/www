</div><br /><em>Abstract:<br /></em><br />High Performance Computing has been the backbone of scientific computing for decades.  Driven primarily by simulation, larger and larger computing systems have been built to solve problems of incredible scale and complexity.  The rise of the web and cloud computing has not replaced these large scale engines, but rather has augmented them, allowing increasingly complex scientific workflows using HPC in conjunction with cloud technologies -- containers, Rest APIs, manages services, etc.   In this talk, I will discuss how the Texas Advanced Computing Center (TACC) at UT Austin is responding to these trends; both in the way we design, deploy and support our large scale HPC systems, and how the science we support uses an ever increasing array of cyberinfrastructure services. <br /><br /><br /><em>About the speaker:<br /></em><br />Dr. Stanzione is the Executive Director of the Texas Advanced Computing Center (TACC) at The University of Texas at Austin since July 2014, previously serving as Deputy Director. He is the principal investigator (PI) for a National Science Foundation (NSF) grant to deploy and support Stampede2, a large scale supercomputer, which will have over twice the system performance of TACC&rsquo;s original Stampede system. Stanzione is also the PI of TACC's Wrangler system, a supercomputer for data-focused applications. For six years he was co-director of CyVerse, a large-scale NSF life sciences cyberinfrastructure. Stanzione was also a co-principal investigator for TACC's Ranger and Lonestar supercomputers, large-scale NSF systems previously deployed at UT Austin. Stanzione received his bachelor's degree in electrical engineering and his master's degree and doctorate in computer engineering from Clemson University.<br /></div></div></div><div class="re-col-desk-4 re-col-tabl-4 re-col-tab-6 re-col-mob-12"><div id='stacks_out_139_page18' class='stacks_out'><div id='stacks_in_139_page18' class='stacks_in '><div id=