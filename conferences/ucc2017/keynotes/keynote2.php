</div><em><br />Abstract:<br /></em><br />Jetstream1, built with OpenStack, is the first production cloud funded by the NSF for conducting general-purpose science and engineering research as well as an easy-to-use platform for education activities. Unlike many high-performance computing systems, Jetstream uses the interactive Atmosphere graphical user interface developed as part of the iPlant (now CyVerse) project and focuses on interactive use on uniprocessor or multiprocessor. This interface provides for a lower barrier of entry for use by educators, students, practicing scientists, and engineers. A key part of Jetstream&rsquo;s mission is to extend the reach of the NSF&rsquo;s eXtreme Digital (XD) program to a community of users who have not previously utilized NSF XD program resources, including those communities and institutions that traditionally lack significant cyberinfrastructure resources. OpenStack deployments all have the same five basic services: identity, images, block storage, networking, and compute. There are additional services offered; however, by and large, they are underutilized. The use of these services will be discussed as well as highlights from the first year of production operations, and future plans for the project.<br /><br /><em>About the speaker:<br /></em><br />David Hancock is the program director for advanced cyberinfrastructure within the Indiana University Pervasive Technology Institute's Research Technologies division. Hancock is responsible for directing IU's local and national high-performance computing (HPC) systems and cloud resources for research. Hancock is a senior investigator and acting primary investigator for the Jetstream project funded by the National Science Foundation (NSF). He is also responsible for directing IU system administrators who participate in the NSF XSEDE project as well as being senior personnel on a number of other completed NSF projects. Hancock is also an active participant in a number of national and international HPC organizations and is currently serving as the president of the Cray User Group and a representative in the XSEDE Service Provider (SP) Forum. Previously he served as the vice president for the IBM HPC User Group, vice president and program chair for the Cray User Group, and vice chair of the XSEDE SP Forum.<br /></div></div></div><div class="re-col-desk-4 re-col-tabl-4 re-col-tab-6 re-col-mob-12"><div id='stacks_out_139_page15' class='stacks_out'><div id='stacks_in_139_page15' class='stacks_in '><div id='stacks_out_p139_n53_page15' class='stacks_out'><div id='stacks_in_p139_n53_page15' class='stacks_in '><div id='stacks_out_pp139_n53_n7_page15' class='stacks_out'><div id='stacks_in_pp139_n53_n7_page15' class='stacks_in text_stack'><h3>Hosting Organizations</h3></div></div><div id='stacks_out_pp139_n53_n9_page15' class='stacks_out'><div id='stacks_in_pp139_n53_n9_page15' class='stacks_in image_stack'>
